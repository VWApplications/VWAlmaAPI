# Internationalization

# Código de idioma para esta instalação. Todas as opções podem ser encontradas aqui:
# http://www.i18nguy.com/unicode/language-identifiers.html
PORTUGUESE = 'pt-BR'
ENGLISH = 'en-us'
LANGUAGE_CODE = PORTUGUESE

# Fuso horário local para esta instalação. As opções podem ser encontradas aqui:
# http://en.wikipedia.org/wiki/List_of_tz_zones_by_name
# Embora nem todas as opções possam estar disponíveis em todos os sistemas operacionais.
# Em um ambiente Windows, isso deve ser definido como o fuso horário do sistema.
TIME_ZONE = 'America/Sao_Paulo'

# Se você definir como False, o Django fará algumas otimizações para não
# carregar o mecanismo de internacionalização.
USE_I18N = True

# Se você definir como False, o Django não formatará datas, números e
# calendários de acordo com a localidade atual.
USE_L10N = True

# Se você definir como False, o Django não usará data / hora com reconhecimento de fuso horário.
USE_TZ = True